# Multiplication of two arrays [1,2,3,4] [10,20,30,40]
import numpy as multiple
  
in_arr1 = multiple.array([1,2,3,4]) 
in_arr2 = multiple.array([10,20,30,40]) 
   
print ("1st Input array : ", in_arr1) 
print ("2nd Input array : ", in_arr2) 
   
    
out_arr = multiple.multiply(in_arr1, in_arr2)  
print ("Resultant output array: ", out_arr)  